export const standaloneServicesBySlug = Object.fromEntries(
  standaloneServices.map((s) => [s.slug, s]),
) as Record<string, StandaloneService>;

export const getStandaloneBySlug = (slug: string): StandaloneService | undefined =>
  standaloneServicesBySlug[slug];

export const getStandaloneByRoute = (pathname: string): StandaloneService | undefined =>
  standaloneServices.find((s) => s.route === pathname);
